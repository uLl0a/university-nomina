    private void Btn_ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ConfirmActionPerformed
        String name = E_Name.getText();
        String lastname = E_Lastname.getText();
        String marital_status = E_MaritalStatus.getSelectedItem().toString();
        String status = E_Status.getSelectedItem().toString();
        String cedula = E_Cedula.getText();
        String s_salary = E_Salary.getText();
        
        if (name.isEmpty() || lastname.isEmpty() || marital_status.isEmpty() || cedula.isEmpty() || s_salary.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos porfavor", "Intenta de nuevo", ERROR_MESSAGE);
        } else {
            try {
                float salary = Float.parseFloat(s_salary);
                if(EditModeEnable){
                    try {
                        Connection conn = DriverManager.getConnection("jdbc:sqlite:database.db");
                        Backend.EditEmployee(conn,SelectedID, name, lastname, cedula, salary, marital_status, status);
                        conn.close();
                    } catch (SQLException e) {
                        e.printStackTrace();
                        System.out.println(e.getClass().getName() + ": " + e.getMessage());
                    }
                    EditModeEnable = false;
                    jPanel2.setBorder(javax.swing.BorderFactory.createTitledBorder(null, "Nuevo Empleado", javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION, javax.swing.border.TitledBorder.DEFAULT_POSITION, new java.awt.Font("Segoe Print", 1, 18)));
                }else{
                    try {
                        Connection conn = DriverManager.getConnection("jdbc:sqlite:database.db");
                        Backend.AddEmployee(conn,name, lastname, cedula, salary, marital_status, status);
                        conn.close();
                    } catch (SQLException e) {
                        e.printStackTrace();
                        System.out.println(e.getClass().getName() + ": " + e.getMessage());
                    }
                }
                ResetTable();
                ReloadTable();
                JOptionPane.showMessageDialog(this, "Registrado correctamente", "Nomina Actualizada", INFORMATION_MESSAGE);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Completa todos los campos correctamente", "Intenta de nuevo ", ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_Btn_ConfirmActionPerformed

