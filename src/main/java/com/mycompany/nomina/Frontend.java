    public void ResetTable() {
        DefaultTableModel table = (DefaultTableModel) E_table.getModel();
        int count = table.getRowCount();
        while (count > 0) {
            table.removeRow(count - 1);
            count = table.getRowCount();
        }
    }
    
    public void ReloadTable(){
        Enumeration<Integer> e = Backend.GetEmployeeList().keys();
        DefaultTableModel table = (DefaultTableModel) E_table.getModel();
        while(e.hasMoreElements()){
            int key = e.nextElement();
            Object[] rowData = {Backend.GetEmployee(key).GetID(), 
                                Backend.GetEmployee(key).GetCedula(),
                                Backend.GetEmployee(key).GetName(),
                                Backend.GetEmployee(key).GetLastname(),
                                Backend.GetEmployee(key).GetSalary(),
                                Backend.GetEmployee(key).CalcAfp(),
                                Backend.GetEmployee(key).CalcArs(),
                                Backend.GetEmployee(key).CalcIsr(),
                                Backend.GetEmployee(key).CalcSueldoNeto(),
                                Backend.GetEmployee(key).GetMaritalStatus(),
                                Backend.GetEmployee(key).GetStatus()}; 
            table.addRow(rowData);
        }
    }

